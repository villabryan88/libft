#include "libft.h"

void	ft_putnbr(int n)
{
	int		neg;

	neg = 1;
	if (n < 0)
	{
		ft_putchar('-');
		neg = -1;
	}
	if (n > 9 || n < -9)
		ft_putnbr((n / 10) * neg);
	ft_putchar((n % 10) * neg + '0');
}
